package org.drasyl.util;

import java.util.regex.Pattern;

/**
 * Utility class for operations on {@link InetAddress}s.
 */
public final class InetAddressUtil {
    // https://mkyong.com/regular-expressions/how-to-validate-ip-address-with-regular-expression/
    private static final Pattern IP4_PATTERN = Pattern.compile("^(([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])(\\.(?!$)|$)){4}$");
    // https://stackoverflow.com/questions/53497/regular-expression-that-matches-valid-ipv6-addresses
    private static final Pattern IP6_PATTERN = Pattern.compile("(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))");
    // https://stackoverflow.com/a/166157/1074188
    private static final int MAX_ADDRESS_LENGTH = 45;

    private InetAddressUtil() {
        // util class
    }

    /**
     * Returns {@code true} if {@code address} is a valid. Otherwise {@code false} is returned.
     *
     * @param address the string to be checked
     * @return {@code true} if {@code address} is a valid. Otherwise {@code false} is returned
     */
    public static boolean isInetAddress(final String address) {
        if (address == null || address.length() > MAX_ADDRESS_LENGTH) {
            return false;
        }

        // IPv6?
        final boolean hasColon = address.indexOf(':') != -1;
        if (hasColon) {
            final String[] parts = address.split("%", 2);
            return IP6_PATTERN.matcher(parts[0]).matches();
        }

        // IPv4?
        final boolean hasDot = address.indexOf('.') != -1;
        if (hasDot) {
            return IP4_PATTERN.matcher(address).matches();
        }

        return false;
    }
}
