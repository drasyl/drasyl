package org.drasyl.cli.node.message;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;

import static com.fasterxml.jackson.annotation.JsonInclude.Include.NON_NULL;
import static java.util.Objects.requireNonNull;

/**
 * Represents a JSON-RPC 2.0 error object.
 */
public class JsonRpc2Error {
    public static final int PARSE_ERROR = -32700;
    public static final int INVALID_REQUEST = -32600;
    public static final int METHOD_NOT_FOUND = -32601;
    public static final int INVALID_PARAMS = -32602;
    public static final int INTERNAL_ERROR = -32603;
    private final int code;
    private final String message;
    @JsonInclude(NON_NULL)
    private final Object data;

    public JsonRpc2Error(@JsonProperty("code") final int code,
                         @JsonProperty("message") final String message,
                         @JsonProperty("data") final Object data) {
        this.code = code;
        this.message = requireNonNull(message);
        this.data = data;
    }

    public JsonRpc2Error(final int code, final String message) {
        this(code, message, null);
    }

    @Override
    public String toString() {
        return "JsonRpc2Error{" +
                "code=" + code +
                ", message='" + message + '\'' +
                ", data=" + data +
                '}';
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }

    public Object getData() {
        return data;
    }
}
